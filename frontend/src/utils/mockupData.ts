export function getBusinessProcessRiskDashboardData(
  orgId: string | null = null
): BusinessProcessDashboardItem[] {
  const filteredDataByOrg = orgId
    ? riskMockupData.filter((item) => item.orgId === orgId)
    : riskMockupData;

  const grouped: Record<string, BusinessProcessDashboardItem> = {};

  filteredDataByOrg.forEach((item) => {
    const key = `${item.businessUnitId}__${item.businessProcessId}`;

    if (!grouped[key]) {
      grouped[key] = {
        businessUnitName: item.businessUnit,
        processName: item.businessProcess,
        severity: item.processSeverity || "",
        riskAppetite: item.riskAppetite,
        maxRiskExposure: item.riskExposure,
        maxNetExposure: item.netExposure,
        assets: [],
        risks: [],
      };
    }

    // --- Update max exposure values ---
    grouped[key].maxRiskExposure = Math.max(
      grouped[key].maxRiskExposure,
      item.riskExposure
    );

    grouped[key].maxNetExposure = Math.max(
      grouped[key].maxNetExposure,
      item.netExposure
    );

    // --- Add unique assets ---
    if (!grouped[key].assets.some((a) => a.applicationName === item.asset)) {
      grouped[key].assets.push({
        applicationName: item.asset,
        controlStrength: item.controlStrength,
        targetStrength: item.targetStrength,
      });
    }

    // --- Add risks ---
    grouped[key].risks.push({
      riskScenario: item.riskScenario,
      riskScenarioCIAMapping: item.riskScenarioCIAMapping,
      riskExposure: item.riskExposure,
      riskExposureLevel: item.riskExposureLevel,
      netExposure: item.netExposure,
      netExposureLevel: item.netExposureLevel,
    });
  });

  return Object.values(grouped);
}

export function getBusinessUnitSeverityData(orgId: string | null = null) {
  const filteredDataByOrg = orgId
    ? riskMockupData.filter((item) => item.orgId === orgId)
    : riskMockupData;

  const severity = ["Very Low", "Low", "Moderate", "High", "Critical"];
  const distinctBusinessUnits = new Set(
    filteredDataByOrg.map((i) => i.businessUnit)
  );
  const result: { x: string; y: string; value: number }[] = [];

  distinctBusinessUnits.forEach((i) => {
    severity.forEach((j) => {
      result.push({
        x: j,
        y: i,
        value: filteredDataByOrg.filter(
          (item) => item.riskExposureLevel === j && item.businessUnit === i
        ).length,
      });
    });
  });
  console.log(result)
  return result;
}
